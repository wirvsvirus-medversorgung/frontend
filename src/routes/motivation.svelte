<h2>Unsere Motivation</h2>

<p>COVID-19 führt global zu einer starken Belastung der Menschen und Ressourcen des Gesundheitssystem.
Es zeichnet sich ab, dass auch Personal im deutschen Gesundheitswesen eine immer knapper werdende Ressource sein wird.</p>
<p>Viele Medizinstudierende sind sehr engagiert und voller Tatkraft, um Krankenhäuser, Ambulanzen der kassenärztlichen
Vereinigungen und niedergelassene Praxen, zu unterstützen. Durch den verschobenen Semesterbeginn sind viele Medizinstudierende zeitlich flexibel.</p>
<p>Mit MedSupport möchten wir helfen, die zahlreichen Medizinstudierenden mit allen Institutionen zu verbinden, damit Unterstützung dort ankommt, wo sie am meisten benötigt wird. 
#wirvsvirus</p>

<h2>Die Idee hinter unserem Projekt</h2>
<p>Zunächst registriert man sich als Studierender mit Angabe von Vorerfahrungen und Kompetenzen. Wichtig ist auch regionale Standorte mit Radius anzugeben, damit menschliche Ressourcen flächenhaft und fair verteilt werden. Auch verschiedene Institutionen registrieren sich mit den Angaben, wie viele Helfer/-innen benötigt werden, welche Kompetenzen gefragt sind und in welchen Bereichen Helfer/-innen eingesetzt werden sollen. Danach erhält der Studierende eine Liste mit vorgeschlagenen Institutionen, die nach Dringlichkeit und unter Beachtung der regionalen Angaben zugeordnet wurden.</p>
<p>Beim Klick auf einen Eintrag kann man Kontakt aufnehmen und als Helfer direkt registrieren - MedSupport sorgt dafür dass die Unterstützung dort ankommt, wo sie am meisten gebraucht wird.</p>
