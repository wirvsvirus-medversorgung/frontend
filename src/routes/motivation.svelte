<h2>Unsere Motivation</h2>

<p>COVID-19 führt global zu einer starken Belastung der Menschen und Ressourcen des Gesundheitssystem.
Es zeichnet sich ab, dass auch Personal im deutschen Gesundheitswesen eine immer knapper werdende Ressource sein wird.</p>
<p>Viele Medizinstudierende sind sehr engagiert und voller Tatkraft, um Krankenhäuser, Ambulanzen der kassenärztlichen
Vereinigungen und niedergelassene Praxen, zu unterstützen. Durch den verschobenen Semesterbeginn sind viele Medizinstudierende zeitlich flexibel.</p>
<p>Mit MedSupport möchten wir helfen, die zahlreichen Medizinstudierenden mit allen Institutionen zu verbinden, damit Unterstützung dort ankommt, wo sie am meisten benötigt wird. 
#wirvsvirus</p>